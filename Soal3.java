public class Soal3 {
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        // Mendapatkan kode ASCII dari karakter 'X' dan 'y'
        int asciiX = (int) 'X';
        int asciiY = (int) 'y';

        // Menjumlahkan kode ASCII dari kedua karakter
        int sum = asciiX + asciiY;

        // Output hasil penjumlahan
        System.out.println("Kode ASCII dari karakter 'x': " + asciiX);
        System.out.println("Kode ASCII dari karakter 'y': " + asciiY);
        System.out.println("Hasil penjumlahan kode ASCII dari karakter 'X' dan 'y' adalah: " + sum);
    }
}

   
    

